#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	const char	new_c = (char) c;
	size_t		i;

	i = 0;
	while (s[i] && s[i] != new_c)
		i++;
	if (s[i] != new_c)
		return (FT_NULL);
	return ((char *)s + i);
}
